public class RelationalOperators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        num2 = num1;

        if (num1 > num2){
            System.out.println("num1 is greater then num2");
        }
        if (num1 < num2){
            System.out.println("num1 is less then num2");
        }
        if (num1 == num2){
            System.out.println("num1 is equal to num2");
        }
  }
}